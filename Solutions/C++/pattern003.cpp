#include <iostream>
using namespace std;

int main(void)
{
    int i, j;
    for (i = 1; i <= 5; i++)
    {
        for (j = 1; j <= 5; j++)
        {
            cout << j;
        }
        cout << endl;
    }
    return 0;
}